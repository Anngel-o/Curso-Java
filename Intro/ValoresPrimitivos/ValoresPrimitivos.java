package Java.Intro.ValoresPrimitivos;

public class ValoresPrimitivos {
    public static void main(String[] args) {
        /*
         * Variables Enteras
         */

        byte entero = 120; // 8 bits valores de -128 a 127
        short entero2 = 1234; // 16 bits valores de -32 768 a 32,767
        int entero3 = 12484538; // 32 bits valores de -2 147 483 648 a 2 147 483 647
        long entero4 = 1234556746; // 64 bits valores de
        // -9 223 372 036 854 775 808 a 9 223 372 036 854 775 808

        /*
         * Variables con decimal
         */

        double decimal = 34.5; // 64 bits valores de 4.9 e-324 a 1.8 e+308
        float decimal2 = 3.5f; // 32 bits valores de 1.4 e-045 a 3.4 e+038

        /*
         * Valor caracter
         */

        char caracter = 'e';

        /*
         * Variables logicas
         */
        boolean decision = true;
    }
}
