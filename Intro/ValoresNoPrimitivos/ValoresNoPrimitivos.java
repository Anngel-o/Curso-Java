package Java.Intro.ValoresNoPrimitivos;

public class ValoresNoPrimitivos {
    public static void main(String[] args) {
        Integer num = 10; // almacena numeros
        Integer num2 = null; // almacena valores vacios
        String palabra = "Hola que tal";
        final int num4 = 7; // constante, siempre valdra 7

        System.out.println(num);
        System.out.println(num2);
        System.out.println(palabra);
        System.out.println(num4);
    }
}
